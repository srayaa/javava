package cn.javava.lottery.util;

public class AESException extends RuntimeException
{
	public AESException()
	{
		super();
	}

	public AESException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public AESException(String message)
	{
		super(message);
	}

	public AESException(Throwable cause)
	{
		super(cause);
	}

}
